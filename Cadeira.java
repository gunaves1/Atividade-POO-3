package atividadesPOO3;

public class Cadeira {
	String cor;
	int rodas;
	String material;
	int bracos;
	String abaixa;
	String sobe;
	String andaFrente;
	String andaTras;
	
	public String getCor() {
		return cor;
	}
	public void setCor (String cor) {
		this.cor = cor;
	}
	public int getRodas() {
		return rodas;
	}
	public void setRodas (int rodas) {
		this.rodas = rodas;
	}
	public String getMaterial() {
		return material;
	}
	public void setMaterial (String material) {
		this.material = material;
	}
	public int getBracos() {
		return bracos;
	}
	public void setBracos (int bracos) {
		this.bracos = bracos;
	}
	public String getAbaixa() {
		return abaixa;
	}
	public void setAbaixa (String abaixa) {
		this.abaixa = abaixa;
	}
	public String getSobe() {
		return sobe;
	}
	public void setSobe (String sobe) {
		this.sobe = sobe;
	}
	public String getAndaFrente() {
		return andaFrente;
	}
	public void setAndaFrente (String andaFrente) {
		this.andaFrente = andaFrente;
	}
	public String getAndaTras() {
		return andaTras;
	}
	public void setAndaTras (String andaTras) {
		this.andaTras = andaTras;
	}
}
