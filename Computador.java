package atividadesPOO3;

public class Computador {
	String cor;
	int entradas;
	int equipamentos;
	String marca;
	String liga;
	String desliga;
	String brilha;
	String fioMouse;

	public String getCor() {
		return cor;
	}
	public void setCor (String cor) {
		this.cor = cor;
	}
	public int getEntradas() {
		return entradas;
	}
	public void setEntradas (int entradas) {
		this.entradas = entradas;
	}
	public int getEquipamentos() {
		return equipamentos;
	}
	public void setEquipamentos (int equipamentos) {
		this.equipamentos = equipamentos;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca (String marca) {
		this.marca = marca;
	}
	public String getLiga() {
		return liga;
	}
	public void setLiga (String liga) {
		this.liga = liga;
	}
	public String getDesliga() {
		return desliga;
	}
	public void setDesliga (String desliga) {
		this.desliga = desliga;
	}
	public String getBrilha() {
		return brilha;
	}
	public void setBrilha (String brilha) {
		this.brilha = brilha;
	}
	public String getFioMouse() {
		return fioMouse;
	}
	public void setFioMouse (String fioMouse) {
		this.fioMouse = fioMouse;
	}
}