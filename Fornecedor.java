package atividadesPOO3;

public class Fornecedor {
	int id;
	String razaoSocial, nomeFantasia, cnpj, endereco, fone, email, inscricaoEstadual, entregar, atender;
	
	public String getRazaoSocial() {
		return razaoSocial;
	}
	public void setRazaoSocial (String razaoSocial) {
		this.razaoSocial = razaoSocial;
	}
	public String getNomeFantasia() {
		return nomeFantasia;
	}
	public void setNomeFantasia (String nomeFantasia) {
		this.nomeFantasia = nomeFantasia;
	}
	public String getCnpj() {
		return cnpj;
	}
	public void setCnpj (String cnpj) {
		this.cnpj = cnpj;
	}
	public String getEndereco() {
		return endereco;
	}
	public void setEndereco (String endereco) {
		this.endereco = endereco;
	}
	public String getFone() {
		return fone;
	}
	public void setFone (String fone) {
		this.fone = fone;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail (String email) {
		this.email = email;
	}
	public String getInscricaoEstadual() {
		return inscricaoEstadual;
	}
	public void setInscricaoEstadual (String inscricaoEstadual) {
		this.inscricaoEstadual = inscricaoEstadual;
	}
	public String getEntregar() {
		return entregar;
	}
	public void setEntregar (String entregar) {
		this.entregar = entregar;
	}
	public String getAtender() {
		return atender;
	}
	public void setAtender (String atender) {
		this.atender = atender;
	}
}